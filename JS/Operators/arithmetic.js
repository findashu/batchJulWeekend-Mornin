// Arithmetic Operators

// Addition +
// Subtraction -
// Multiplication *
// Division /
// Modulus %
// Increment ++
// Decrement --

// addition

var addition = 10 + 2;

console.log(addition);

var add = addition + 10;

console.log(add);

// Substraction

var subs = 10 - 15;
console.log(subs);


var mul = 12 * 5;

console.log(mul);


var div = 31 / 3;

console.log(div);


// modulus

var mod = 31 % 3;
console.log(mod);


// Increment

var a = 10;

var b = 10;

console.log(a++);
console.log(a);
console.log(++b);
// post Increment
a++;

// pre Increment
++b;

console.log('Post', a);
console.log('Pre', b);

// Decrement

var c = 20;
var d = 10;

console.log(c--);

console.log(c);

console.log(--d)
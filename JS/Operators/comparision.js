// Comparision Operator

// Equal ==
// Not Equal !=
// Greater than >
// Greater than equals >=
// Less than <
// Less than equals <=
// Strict Equal ===
// Strict Not Equal !==

var a = 120;
var b = 120;

console.log(a == b);

console.log(a != b);

console.log(a > b);

console.log(a >= b);


console.log( a < b);

console.log(a <= b);

// Strict Equal

var c = 10;
var d = '10';

console.log(c === d);

console.log(c !== d);